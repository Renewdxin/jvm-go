// int 逻辑右位移
type IUSHR struct{ base.NoOperandsInstruction }

// int变量仅32位，golang位移操作符右侧必须是无符号数，需要进行类型转换
func (self *IUSHR) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	result := int32(uint32(v1) >> s)
	stack.PushInt(result)
}

